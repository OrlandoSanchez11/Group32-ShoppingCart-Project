    public void insertCartRecord(String productId, String productName, String pPrice, String quantity) {
        Connection con = MyConnection.getConnection();
        String query = null;
        if (con != null) {
                query = "INSERT INTO cart (pid, pname, pqty, pprice, buyer_name) VALUES (?, ?, ?, ?, ?)";
                try (PreparedStatement pstmt = con.prepareStatement(query)) {
                    pstmt.setInt(1, Integer.parseInt(productId));
                    pstmt.setString(2, productName);
                    pstmt.setInt(3, Integer.parseInt(quantity));
                    pstmt.setFloat(4, Float.parseFloat(pPrice));
                    pstmt.setString(5, login.name);

                    int rowsAffected = pstmt.executeUpdate();

                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(this, "Record inserted successfully in cart table.", null, JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Failed to insert record in cart table.", "error", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            

            // Close the connection when done
            try {
                con.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
