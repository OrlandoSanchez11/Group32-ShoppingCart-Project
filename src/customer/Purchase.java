    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "No row selected.", "error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String productId = String.valueOf(model.getValueAt(selectedRow, 0));
        String productName = (String) model.getValueAt(selectedRow, 1);
        String pPrice = String.valueOf(model.getValueAt(selectedRow, 3));
        String availableQuantity = String.valueOf(model.getValueAt(selectedRow, 2));
        String quantity = jTextField2.getText();
        
        if (Integer.parseInt(quantity) > Integer.parseInt(availableQuantity)) {
            JOptionPane.showMessageDialog(this, "The product isn't available in the desired quantity.", "error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        insertCartRecord(productId, productName, pPrice, quantity);
        
    }//GEN-LAST:event_jButton1MouseClicked

